    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        if(txtNama.getText().equalsIgnoreCase("")&&txtPassword.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Data masih kosong!");
        }else if(txtNama.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Nama belum diisi!");
            txtNama.requestFocus();
        }else if(txtPassword.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Password belum diisi!");
            txtPassword.requestFocus();
        }else{
            simpan();
        }
    }//GEN-LAST:event_btSaveActionPerformed

