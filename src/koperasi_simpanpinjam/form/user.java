    @Override
    public void bersih() {
        txtNama.setText("");
        txtPassword.setText("");
        cmbAkses.setSelectedIndex(0);
        txtCari.setText("");
        txtUser.setText(uCont.nomorOtomatis());
        showTableUser();
        txtNama.requestFocus();
    }

    @Override
    public void simpan() {
        user=uCont.findUser(txtUser.getText());
        User us=new User();
        if(user==null){
            us.setKodeUser(txtUser.getText());
            us.setNamaUser(txtNama.getText());
            us.setPassword(txtPassword.getText());
            us.setHakAkses(cmbAkses.getSelectedItem().toString());
            try{
                uCont.save(us);
            }catch(Exception ex){}
            JOptionPane.showMessageDialog(null, "Data telah tersimpan!");
        }else{
            us.setKodeUser(txtUser.getText());
            us.setNamaUser(txtNama.getText());
            us.setPassword(txtPassword.getText());
            us.setHakAkses(cmbAkses.getSelectedItem().toString());
            try{
                uCont.update(us);
            }catch(Exception ex){}
            JOptionPane.showMessageDialog(null, "Data telah di update!");
        }
        bersih();
    }

    @Override
    public void hapus() {
        try{
            uCont.delete(txtUser.getText());
            JOptionPane.showMessageDialog(null, "Data telah di hapus!");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Data tidak bisa di hapus karena "+ex.getMessage());
        }
        bersih();
    }

