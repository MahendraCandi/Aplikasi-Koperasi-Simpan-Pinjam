    private void cariTable(String cari){
        DefaultTableModel x=pCont.cari(model, cari);
        if(x.getRowCount()==0){
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan!");
        }else{
            tablePinjam.setModel(pCont.cari(model, cari));
        }
    }

