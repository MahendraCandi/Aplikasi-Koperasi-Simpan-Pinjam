    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int baris=tablePinjam.getSelectedRow();
        if(baris==-1){
            JOptionPane.showMessageDialog(null, "Pilih baris!");
        }else{
            AP.no=tablePinjam.getValueAt(baris, 0).toString();
            AP.ang=tablePinjam.getValueAt(baris, 1).toString();
            AP.tot=tablePinjam.getValueAt(baris, 6).toString();
            AP.bi=tablePinjam.getValueAt(baris, 7).toString();
            AP.pilihNomor();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

